package com.hepolite.coreutil.movement;

public enum MovementType
{
	/** In water, bobbing in one spot, not moving */
	FLOATING,
	/** Flying through the air, moving */
	FLYING,
	/** Gliding through the air, moving */
	GLIDING,
	/** Flying, hovering in one spot, not moving */
	HOVERING,
	/** Running, moving on ground */
	RUNNING,
	/** Sneaking, moving on ground */
	SNEAKING,
	/** On ground, standing still */
	STANDING,
	/** In water, moving */
	SWIMMING,
	/** On ground, moving */
	WALKING,
}
